def frequent_junk_mix_digit_letters():
    """
    Words such as: 2b 4b 6b 1b 2b 2e 3b 4b 4e 6b
    """

    def build():
        for d in string.digits:
            for l in string.lowercase:
                yield d + l
                yield l + d

    return ' '.join(build())


